/* Esta clase representa el modelo de datos (models) */
export class Empleado {
    /* Propiedades (Atributos) */
    public nombre: string;
    public edad: number;
    public cargo: string;
    public contratado: boolean;

    /* Constructor */
    public constructor( nombre, edad ) {
        this .nombre = nombre;
        this .edad = edad;
        this .cargo = cargo;
        this .contratado = contratado;
    }
}
